$(document).ready(function() {
	$("#forgot").click(function() {
		$("#setPass").fadeIn("slow");



	});

	$("#save").click(function() {
		$("#setPass").fadeOut("slow");



	});






	$("#createNew").click(function() {
		$("#creation").fadeIn("slow");



	});


	$("#closeCreate").click(function() {
		$("#creation").fadeOut("slow");



	});
});
